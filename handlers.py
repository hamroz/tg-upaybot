def agent_submenu(update: Update, context: CallbackContext) -> int:
    """
    Here we see if user picks '1' => Agent Importer flow
                      or '2' => Agent Exporter flow
    """
    lang = get_user_lang(context)
    choice = update.message.text.strip()

    if choice == "1":
        context.user_data["agent_choice"] = "make_payment"  # import-like
        if lang == "en":
            update.message.reply_text("üåç Enter the recipient country:", reply_markup=ReplyKeyboardRemove())
        else:
            update.message.reply_text("üåç –í–≤–µ–¥–∏—Ç–µ —Å—Ç—Ä–∞–Ω—É –ø–æ–ª—É—á–∞—Ç–µ–ª—è:", reply_markup=ReplyKeyboardRemove())
        return AGENT_IMPORTER_COUNTRY

    elif choice == "2":
        context.user_data["agent_choice"] = "forex_rebate"  # export-like
        if lang == "en":
            update.message.reply_text("üåç Enter the sender's country:", reply_markup=ReplyKeyboardRemove())
        else:
            update.message.reply_text("üåç –í–≤–µ–¥–∏—Ç–µ —Å—Ç—Ä–∞–Ω—É –æ—Ç–ø—Ä–∞–≤–∏—Ç–µ–ª—è:", reply_markup=ReplyKeyboardRemove())
        return AGENT_EXPORTER_COUNTRY

    else:
        # invalid choice => go back to main menu
        return go_back_to_main_menu(update, context)


